psyc.Uniform = function(str) {
    if (str.substr(0,7) != "psyc://") {
	throw("Invalid uniform: " + str);	
    }
    this.uniform = str;
    this.toString = function() {
	return "psyc.Uniform("+str+")";
    };

    str = str.slice(7);

    var pos = str.indexOf("/");

    if (pos == -1) { // root
	this.host = str;
	this.is_user = function() { return 0; }
	this.is_room = function() { return 0; }
    } else {
	this.host = str.substr(0, pos);
	str = str.slice(pos+1);

	this.object = str;
	this.type = str.charCodeAt(0);
	if (this.type == 126) {
	    this.is_user = function() { return 1; }
	    this.is_room = function() { return 0; }
	} else if (this.type == 64) {
	    this.is_user = function() { return 0; }
	    this.is_room = function() { return 1; }

	} else {
	    throw("Invalid uniform: " + this.str);
	}

	pos = str.indexOf("#");

	if (pos != -1) {
	    this.base = str.substr(0, pos);
	    this.channel = str.substr(pos+1, str.length-pos-1);
	} else {
	    this.base = this.object;
	}
    }

};
psyc.AtomParser = function() {
    this.buffer = "";
    this.reset = function() {
	this.type = 0;
	this.length = -1;
    };
    this.reset();
    this.parse = function(str) {
	this.buffer += str;

	var ret = new Array();
	var t = 0;
	while (t = this._parse()) {
	    ret.push(t);
	}
	return ret;
    };
    this._parse = function() {
	if (!this.type) {
	    var pos = this.buffer.indexOf(" ");

	    if (pos == -1) {
		// check here for bogus data
//		if (re[0].search(/(_\w+)+/) != 0) {
//		    throw("bad atom\n");
//		}
		return 0;
	    } else if (pos < 2) {
		throw("bad atom.");
	    }

	    this.type = this.buffer.substr(0, pos);
	    this.buffer = this.buffer.slice(pos+1);
	}

	if (this.length == -1) {
	    var pos = this.buffer.indexOf(" ");

	    if (pos == -1) {
		return 0;
	    } else if (pos == 0) {
		throw("bad atom.");
	    }

	    this.length = parseInt(this.buffer.substr(0, pos));
	    if (this.length < 0 || this.length.toString() != this.buffer.substr(0, pos)) {
		throw("bad length in atom.\n");
	    }
	    this.buffer = this.buffer.slice(pos+1);
	}

	if (this.length > this.buffer.length) {
	    // add a sanity check. we do not want superlarge data strings, i guess
	    return 0;
	}

	var a;

	if (this.length == this.buffer.length) {
	    a = new psyc.Atom(this.type, this.buffer);
	    this.buffer = "";
	} else {
	    a = new psyc.Atom(this.type, this.buffer.substr(0,this.length));
	    this.buffer = this.buffer.slice(this.length);
	}
	this.reset();
	
	return a;
    };
    
};
psyc.find_match = function(obj, key) {
    // i guess we should find direct matches first...

    if (obj[key] != undefined) {
	return obj[key];
    }

    for (var i in obj) {
	if (i.length > key.length
		&& i.substr(0, key.length) == key
		&& i.charAt(key.length) == "_") {
	    return obj[i];
	}
    }
};
psyc.find_abbrev = function(obj, key) {
    var t = key;

    while (t.length && obj[t] == undefined) {
	var i = t.lastIndexOf("_");

	if (i == -1) {
	    return undefined;
	} else {
	    t = t.substr(0, i);
	}
    }

    return obj[t];
};
psyc.Atom = function(type, data) {
    this.type = type;
    this.data = data;
    this.render = function() {
		return this.type + " " + new String(this.data.length) + " " + this.data;
    };
    // we don't really need this (yet), but i just wanted to!
    this.length = function() {
		return this.type.length + new String(this.data.length).length 
			+ this.data.length + 2;
    };
};
psyc.print_vars = function(v) {
    var ret = new Array();
    for (var i in v) {
	if (i.substr(0,1) == "_") 
	    ret.push(i + " : " + this.vars[i]);
    }

    return ret.join(", ");
};
psyc.MMPPacket = function(vars, data) {
    this.vars = vars;
    this.data = data;
    this.source = function() {
	return psyc.find_match(this.vars, "_source");	
    };
    this.target = function() {
	return psyc.find_match(this.vars, "_target");	
    };
    this.toString = function() {
	return "psyc.MMPPacket(" + psyc.print_vars(this.vars) + ", " + this.data + ")";
    }
};
psyc.Message = function(mc, vars, data) {
    this.mc = mc;
    this.toString = function() {
		var ret = "psyc.Packet("+this.mc+", ([ ";
		ret += psyc.print_vars(this.vars);
		ret += "]))";
		return ret;
    };
    if (vars) {
		this.vars = vars;
    } else {
		this.vars = new Array();
    }
    vars.mapping = 1;

    if (data) {
		this.data = data;
    } else {
		this.data = "";
    }
};
psyc.mapping_length = function(a) {
    if (!a.mapping) {
		throw("bad argument to mapping_length()");
    }

    var i = 0;
    for (var t in a) {
		i++;
    }

    return i;
};
